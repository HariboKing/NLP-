def hash_password(password: str) -> str:
    salt = secrets.token_hex(16)
    digest = hashlib.pbkdf2_hmac(
        "sha256",
        password.encode("utf-8"),
        salt.encode("utf-8"),
        120_000,
    )
    return f"{salt}${digest.hex()}"


def verify_password(password: str, password_hash: str) -> bool:
    salt, known_digest = password_hash.split("$", 1)
    computed = hashlib.pbkdf2_hmac(
        "sha256",
        password.encode("utf-8"),
        salt.encode("utf-8"),
        120_000,
    ).hex()
    return hmac.compare_digest(computed, known_digest)


def create_session(connection, user_id: int) -> tuple[str, str]:
    token = secrets.token_urlsafe(32)
    csrf_token = secrets.token_urlsafe(24)
    expires_at = (utc_now() + timedelta(hours=SESSION_HOURS)).isoformat(timespec="seconds")
    connection.execute(
        "INSERT INTO sessions (token, user_id, csrf_token, expires_at) VALUES (?, ?, ?, ?)",
        (token, user_id, csrf_token, expires_at),
    )
    connection.commit()
    return token, csrf_token


def get_session(connection, token: str | None):
    if not token:
        return None

    session = connection.execute(
        """
        SELECT sessions.token, sessions.user_id, sessions.csrf_token, sessions.expires_at,
               users.email, users.first_name, users.last_name, users.is_platform_admin,
               users.account_kind, users.managed_by_user_id
        FROM sessions
        JOIN users ON users.id = sessions.user_id
        WHERE sessions.token = ?
        """,
        (token,),
    ).fetchone()
    if not session:
        return None

    if datetime.fromisoformat(session["expires_at"]) <= utc_now():
        destroy_session(connection, token)
        return None

    return session


def destroy_session(connection, token: str | None) -> None:
    if not token:
        return
    connection.execute("DELETE FROM sessions WHERE token = ?", (token,))
    connection.commit()


