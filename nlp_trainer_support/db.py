class PostgresCursorWrapper:
    def __init__(self, cursor, *, buffered_rows: list[dict[str, Any]] | None = None, lastrowid: int | None = None) -> None:
        self._cursor = cursor
        self._buffered_rows = buffered_rows or []
        self.lastrowid = lastrowid

    def fetchone(self):
        if self._buffered_rows:
            return self._buffered_rows.pop(0)
        return self._cursor.fetchone()

    def fetchall(self):
        rows = list(self._buffered_rows)
        self._buffered_rows.clear()
        rows.extend(self._cursor.fetchall())
        return rows

    @property
    def rowcount(self) -> int:
        return self._cursor.rowcount


class PostgresConnectionWrapper:
    def __init__(self, connection) -> None:
        self._connection = connection

    def __enter__(self):
        return self

    def __exit__(self, exc_type, exc, tb) -> None:
        if exc_type is None:
            self._connection.commit()
        else:
            self._connection.rollback()
        self._connection.close()

    def _translate_sql(self, sql: str) -> str:
        translated = sql
        ignore_insert = bool(re.match(r"^\s*INSERT\s+OR\s+IGNORE\s+INTO\b", translated, flags=re.IGNORECASE))
        if ignore_insert:
            translated = re.sub(
                r"^\s*INSERT\s+OR\s+IGNORE\s+INTO\b",
                "INSERT INTO",
                translated,
                count=1,
                flags=re.IGNORECASE,
            )
        translated = translated.replace("?", "%s")
        translated = translated.replace(
            "INTEGER PRIMARY KEY AUTOINCREMENT",
            "INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY",
        )
        translated = translated.replace(" REAL ", " DOUBLE PRECISION ")
        translated = translated.replace("(REAL ", "(DOUBLE PRECISION ")
        translated = translated.replace(" REAL,", " DOUBLE PRECISION,")
        translated = translated.replace(" REAL\n", " DOUBLE PRECISION\n")
        if ignore_insert:
            translated = translated.rstrip().rstrip(";") + " ON CONFLICT DO NOTHING"
        return translated

    def _inject_returning_id(self, sql: str) -> tuple[str, str | None]:
        match = re.match(r"^\s*INSERT\s+INTO\s+([a-zA-Z_][a-zA-Z0-9_]*)", sql, flags=re.IGNORECASE)
        if not match or "RETURNING" in sql.upper():
            return sql, None
        table_name = match.group(1).lower()
        if table_name not in POSTGRES_ID_TABLES:
            return sql, None
        return sql.rstrip().rstrip(";") + " RETURNING id", table_name

    def execute(self, sql: str, params: tuple | list | None = None):
        translated = self._translate_sql(sql)
        translated, returning_table = self._inject_returning_id(translated)
        cursor = self._connection.cursor()
        try:
            cursor.execute(translated, tuple(params or ()))
        except Exception as exc:
            if psycopg is not None and isinstance(exc, psycopg.IntegrityError):
                raise sqlite3.IntegrityError(str(exc)) from exc
            raise
        if returning_table:
            row = cursor.fetchone()
            lastrowid = row["id"] if row else None
            buffered_rows = [row] if row else []
            return PostgresCursorWrapper(cursor, buffered_rows=buffered_rows, lastrowid=lastrowid)
        return PostgresCursorWrapper(cursor)

    def executescript(self, script: str) -> None:
        statements = [statement.strip() for statement in script.split(";") if statement.strip()]
        for statement in statements:
            self.execute(statement)

    def commit(self) -> None:
        self._connection.commit()

    def rollback(self) -> None:
        self._connection.rollback()

    def close(self) -> None:
        self._connection.close()


def utc_now() -> datetime:
    return datetime.now(timezone.utc)


def utc_now_iso() -> str:
    return utc_now().isoformat(timespec="seconds")


def connect():
    if DB_BACKEND == "postgres":
        if psycopg is None:
            raise RuntimeError("DATABASE_URL is set, but psycopg is not installed.")
        raw_connection = psycopg.connect(DATABASE_URL, row_factory=dict_row)
        return PostgresConnectionWrapper(raw_connection)

    DATA_DIR.mkdir(parents=True, exist_ok=True)
    DB_PATH.parent.mkdir(parents=True, exist_ok=True)
    connection = sqlite3.connect(DB_PATH)
    connection.row_factory = sqlite3.Row
    connection.execute("PRAGMA foreign_keys = ON")
    return connection


def reset_postgres_database(connection) -> None:
    for table_name in POSTGRES_RESET_TABLES:
        connection.execute(f"DROP TABLE IF EXISTS {table_name} CASCADE")


def initialize_database(reset: bool = False) -> None:
    if reset:
        if DB_BACKEND == "postgres":
            with connect() as connection:
                reset_postgres_database(connection)
                connection.commit()
        elif DB_PATH.exists():
            DB_PATH.unlink()

    with connect() as connection:
        create_schema(connection)
        seed_database(connection)
        apply_runtime_migrations(connection)


def validate_seed_exercises() -> None:
    title_counts = Counter(exercise["title"] for exercise in SEED_EXERCISES)
    duplicate_titles = sorted(title for title, count in title_counts.items() if count > 1)
    if duplicate_titles:
        preview = ", ".join(duplicate_titles[:5])
        suffix = " ..." if len(duplicate_titles) > 5 else ""
        raise ValueError(
            f"Duplicate exercise titles detected in seed data ({len(duplicate_titles)}): "
            f"{preview}{suffix}"
        )


